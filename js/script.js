// Create operator functions.

function add(a, b) {
    return a + b
}

function subtract(a, b) {
    return a - b
}

function multiply(a, b) {
    return a * b
}

function divide(a, b) {
    return a / b
}

console.log(add(1, 1))
console.log(subtract(2, 1))
console.log(multiply(2, 3))
console.log(divide(4, 2))